/**
 * @file trackingmanager.h
 * @brief Coordinates multi-worm tracking, video processing, and cross-thread orchestration.
 *
 * Responsibilities:
 *  - Orchestrate end-to-end tracking: initial video processing, per-worm trackers (forward/backward),
 *    frame-atomic merge/split handling, progress aggregation, and finalization.
 *  - Own/coordinate worker threads (video processing, per-worm tracking, optional video saving).
 *  - Persist run metadata (threshold settings, input blobs) and populate merge history in storage.
 *
 * Threading model:
 *  - GUI thread constructs and connects to TrackingManager.
 *  - Video processing workers run on QThreads (chunked).
 *  - Per-worm trackers (forward/backward) run on QThreads.
 *  - Optional VideoSaverWorker runs on a background QThread.
 *  - Cross-thread communication via queued signals/slots; registerMetaTypes() registers custom types.
 *
 * Lifecycle:
 *  - startFullTrackingProcess(...) initializes and launches workers.
 *  - cancelTracking() requests cooperative stop.
 *  - cleanupThreadsAndObjects() joins/cleans workers and clears transient state.
 *  - Emits trackingFinishedSuccessfully(...), trackingCancelled(), or trackingFailed(QString) on terminal states.
 *
 * Key signals:
 *  - overallTrackingProgress(int) — aggregate percent.
 *  - trackingStatusUpdate(QString) — human-readable status (e.g., chunk x/y, saving n%).
 *  - allTracksUpdated(Tracking::AllWormTracks) — incremental consolidated tracks.
 *  - trackingFinishedSuccessfully(QString), trackingFailed(QString), trackingCancelled().
 *
 * Key data structures:
 *  - FrameSpecificPhysicalBlob — per-frame merged-blob record used for merge/split logic.
 *  - m_frameMergeRecords, m_splitResolutionMap — per-frame maps for merge/split tracking.
 *  - m_wormObjectsMap, m_wormTrackersList — track history ownership and live workers.
 */
