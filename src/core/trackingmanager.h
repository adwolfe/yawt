// Constants for matching physical blobs on a frame
// PHYSICAL_BLOB_IOU_THRESHOLD:
//   IoU threshold used to decide whether two bounding boxes (on the same frame) represent the
//   same physical merged blob. Higher values reduce false-positive merges but may split legitimate
//   merged blobs. Tune cautiously; affects merge history and split resolution.
// PHYSICAL_BLOB_CENTROID_MAX_DIST_SQ:
//   Squared pixel distance threshold between centroids to consider blobs as the same physical
//   object on a frame. Kept squared to avoid costly sqrt; e.g., 100.0 ~= 10px radius.
//   Used as a secondary check alongside IoU to stabilize matching under small jitter.
const double PHYSICAL_BLOB_IOU_THRESHOLD = 0.5;
const double PHYSICAL_BLOB_CENTROID_MAX_DIST_SQ = 100.0;

// No longer using paused worm resolution
/**
 * @brief Per-frame record of a distinct physical blob (possibly a merge of multiple worms).
 *
 * Purpose:
 *  - Serves as the canonical representation of "what exists on this frame" independent of which
 *    WormTracker reported it first. Multiple WormTrackers may reference the same physical blob.
 *  - Enables frame-atomic merge/split reasoning by decoupling per-tracker state from per-frame observations.
 *
 * Matching criteria:
 *  - Instances are matched using IoU (PHYSICAL_BLOB_IOU_THRESHOLD) and centroid proximity
 *    (PHYSICAL_BLOB_CENTROID_MAX_DIST_SQ). A successful match indicates the same physical blob.
 *
 * Usage notes:
 *  - uniqueId is generated by TrackingManager (m_nextPhysicalBlobId) and is unique per run.
 *  - participatingWormTrackerIDs tracks all conceptual worm IDs present in the blob on this frame.
 *  - selectedByWormTrackerId indicates which tracker chose this blob post-split (if any).
 */
// Represents a distinct physical (merged) blob observed on a specific frame.
struct FrameSpecificPhysicalBlob {
    int uniqueId;                           // Globally unique ID for this physical blob instance
    QRectF currentBoundingBox;
    cv::Point2f currentCentroid;            // Centroid of currentBoundingBox
    double currentArea;
    std::vector<cv::Point> contourPoints;   // Raw contour points in video coordinates (used for merge/split logic)
    QSet<int> participatingWormTrackerIDs;  // Conceptual IDs of WormTrackers in this blob on this frame
    int frameNumber;                        // The frame this blob exists on
    int selectedByWormTrackerId;            // Which WT has selected this blob as their target (-1 = unselected)
    // QDateTime timeFirstReported;         // When this physical blob was first noted on this frame

    FrameSpecificPhysicalBlob() : uniqueId(-1), currentArea(0.0), frameNumber(-1), selectedByWormTrackerId(-1) {}
};

